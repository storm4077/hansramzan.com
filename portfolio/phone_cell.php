				<p>In order to stop people using their phone unnecessarily before bed, it will be locked away in the phone cell and cannot be removed. To prevent people automatically depending on their phones in the morning, the user will place their phone into the Phone Cell which will electronically lock when the device is connected and closed. In the morning, in order for the user to be reunited with their phone, they must wear the Cell Key (with either; the wrist strap, magnetic clip, or a lone module) and take 20 steps (approximately). This gives the user time to carry out their morning routine (i.e. go downstairs, brush teeth, have a shower etc). Upon returning, 20 steps would be completed, allowing the user to wirelessly contact the Cell which will connect via NFC. If the user has completed their 20 steps, the Cell will detect this and unlock the phone.</p>

				<p>A user can set the time in which they want ‘freedom’ away from their phone. They can set exercise targets for themselves which will unlock their phone as and when they complete these targets. i.e. A user sets their phone to ‘lock’ itself (via an app) at 5pm and to unlock itself upon burning 100 calories. They then place their phone into the docking station which automatically locks too (removing physical temptation). The user wears the Key and will go for a run. The Key will notify the user of when they have completed their target in which they can choose to return from their run. In the same way as aforementioned, the user wirelessly contacts the Cell which will connect via NFC. If the user has completed burnt 100 calories, the Cell will detect this and unlock the phone.</p>

