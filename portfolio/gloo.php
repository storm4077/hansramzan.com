			<div class=portfolio_information>
				<p>Unfortunately, smart speakers currently on the market are not tailored to each user. If a user talks in a specific dialect or colloquialism, most devices may not understand the user. "Gloo", on the other hand, is extremely intuitive and can learn from anyone. It is designed to learn naturally from the user rather than solely relying on a logical script. For example;</p>
				<br>
				<ul>
					<li>
						User: "Where can I get a munch in SoCal?"
					</li>
					<li>
						Gloo: "Sorry, I don't understand, please explain?" 
					</li>
					<li>
						User: "What are some good restaurants in South California?"
					</li>
				</ul>
				<p>From this, the device learns to "translate" the first question. As the user communicates more with the speaker, it will eventually understand that "SoCal" means "South California".</p>

				<p>Designed to resemble the human form both by look and feel, ‘Gloo’ encourages people to treat it like a human and converse politely with it. Moreover, ‘Gloo’ has a ‘virtual age’. This means, the more it learns, the older and wiser it becomes! This further adds to the emotional connection that the user creates with the device, almost like raising a child. The companion app also allows a range of other features such as choosing a male, female or gender-fluid voice (inspired by last year’s winner of the digital Design of the Year award – anatomy of an AI system).</p>

